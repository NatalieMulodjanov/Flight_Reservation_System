    private void cancelTicketActionPerformed(java.awt.event.ActionEvent evt) {
        Label messageLabel = new Label();
        messageLabel.setVisible(true);
        messageLabel.setBounds(0, 250, 300, 25);
        textFieldPanel.add(messageLabel);
        try {
            int ticketNo = Integer.parseInt(((TextField) textFieldPanel.getComponent(0)).getText());
            int passNo = Integer.parseInt(((TextField) textFieldPanel.getComponent(1)).getText());
            if (controller.cancelFlight(ticketNo, passNo)) {
                messageLabel.setText(rb.getString("ticketCancelled"));
            } else {
                messageLabel.setText(rb.getString("failedTicketNotFound"));
                messageLabel.setForeground(Color.red);
            }
        } catch (Exception e) {
            messageLabel.setText(rb.getString("failedToCancelTicket") + e.getMessage());
            messageLabel.setForeground(Color.red);
            messageLabel.revalidate();
            messageLabel.repaint();
        }
    }

    private void issueTicketActionPerformed(java.awt.event.ActionEvent evt) {
        Label messageLabel = new Label();
        messageLabel.setVisible(true);
        messageLabel.setBounds(0, 250, 300, 25);
        textFieldPanel.add(messageLabel);
        try {
            Client client = new Client((Integer.parseInt(((TextField) textFieldPanel.getComponent(1)).getText())),
                    ((TextField) textFieldPanel.getComponent(2)).getText(),
                    ((TextField) textFieldPanel.getComponent(3)).getText());
            controller.addToClientTable(client);
            controller.issueTicket(((TextField) textFieldPanel.getComponent(0)).getText(), client);
            messageLabel.setText(rb.getString("ticketIssued"));
        } catch (Exception e) {
            messageLabel.setText(rb.getString("failedToAddClient") + e.getMessage());
            messageLabel.setForeground(Color.red);
            messageLabel.revalidate();
            messageLabel.repaint();
        }
    }

