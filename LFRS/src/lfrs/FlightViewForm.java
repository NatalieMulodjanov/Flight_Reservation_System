    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {
        try {
            String textFieldJoined = "";
            for (Component comp : textFieldPanel.getComponents()) {
                if (comp instanceof TextField) {
                    TextField textField = (TextField) comp;
                    textFieldJoined += textField.getText() + ",";
                }
            }

            String[] splittedValues = textFieldJoined.split(",");
            Flight flightToAdd = new Flight(splittedValues[0], splittedValues[1], splittedValues[2], splittedValues[3], Integer.parseInt(splittedValues[4]), Integer.parseInt(splittedValues[5]), Integer.parseInt(splittedValues[6]), Double.parseDouble(splittedValues[7]));

            controller.addFlight(flightToAdd);
            controller.updateFlightView();

            for (Component comp : textFieldPanel.getComponents()) {
                if (comp instanceof TextField) {
                    TextField textField = (TextField) comp;
                    textField.setText("");
                }
            }
        } catch (Exception ex) {

        }
    }

