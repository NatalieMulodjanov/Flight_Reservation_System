    private void updateFlightActionPerformed(java.awt.event.ActionEvent evt) {
        String flightNo = ((TextField) textFieldPanel.getComponent(0)).getText();
        String field = ((TextField) textFieldPanel.getComponent(1)).getText();
        String newValue = ((TextField) textFieldPanel.getComponent(2)).getText();

        if (newValue.equalsIgnoreCase("duration") || newValue.equalsIgnoreCase("seats") || newValue.equalsIgnoreCase("available")) {
            Integer.parseInt(newValue);
        } else if (newValue.equalsIgnoreCase("amount")) {
            Double.parseDouble(newValue);
        }
        controller.updateFlightData(flightNo, field, newValue);
        controller.updateFlightView();
    }

